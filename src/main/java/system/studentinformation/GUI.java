    private void filter(String s) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        jTable1.setRowSorter(tr);
        
        tr.setRowFilter(RowFilter.regexFilter(s));
    }
    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        int i = jTable1.getSelectedRow();
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
         
        if (StudentIDTF.getText().isEmpty() || FirstNameTF.getText().isEmpty() || LastNameTF.getText().isEmpty() || CourseTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cannot Update\nPlease enter all fields", "Error", JOptionPane.OK_OPTION);
        }
         
        if (i >= 0){
            model.setValueAt(StudentIDTF.getText(), i, 0);
            model.setValueAt(FirstNameTF.getText(), i, 1);
            model.setValueAt(LastNameTF.getText(), i, 2);
            model.setValueAt(CourseTF.getText(), i, 3);
        } else {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_UpdateBtnActionPerformed

